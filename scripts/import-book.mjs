// ---------- SQL escape helpers ----------
const q = (v) => {
  if (v === null || v === undefined) return "NULL";
  if (typeof v === "number") return String(v);
  return `'${String(v).replace(/'/g, "''")}'`;
};

const lines = [];
lines.push("-- Auto-generated by scripts/import-book.mjs. Do not edit by hand.");
lines.push(`-- Book: ${book}`);
lines.push(`-- Generated: ${new Date().toISOString()}`);
// D1 remote doesn't allow explicit BEGIN TRANSACTION; omit it.

// Wipe existing rows for this book so re-imports are clean. Real edits
// shouldn't have happened yet in v0; if they have, don't re-import.
lines.push(`DELETE FROM tn_rows  WHERE book = ${q(book)};`);
lines.push(`DELETE FROM tq_rows  WHERE book = ${q(book)};`);
lines.push(`DELETE FROM twl_rows WHERE book = ${q(book)};`);
lines.push(`DELETE FROM verses   WHERE book = ${q(book)};`);
lines.push(`DELETE FROM book_usfm_meta WHERE book = ${q(book)};`);

// ---------- USFM verses ----------
function importVerses(bibleVersion, srcPath) {
  if (!existsSync(srcPath)) return 0;
  const raw = readFileSync(srcPath, "utf8");
  const json = usfm.toJSON(raw);
  // Stash the USFM headers (\id, \h, \toc*, \mt1, …) so nightly export can
  // emit them verbatim. Without this, the export would synthesize a minimum
  // set and lose the real book titles.
  if (Array.isArray(json.headers) && json.headers.length > 0) {
    lines.push(
      `INSERT OR REPLACE INTO book_usfm_meta (book, bible_version, headers_json) VALUES (${q(book)}, ${q(bibleVersion)}, ${q(JSON.stringify(json.headers))});`,
    );
  }
  let count = 0;
  for (const chapter of Object.keys(json.chapters || {})) {
    const chNum = parseInt(chapter, 10);
    if (Number.isNaN(chNum)) continue;
    const chapterObj = json.chapters[chapter];
    for (const verse of Object.keys(chapterObj)) {
      let vNum;
      let vEnd = null;
      if (verse === "front") {
        // Chapter-front pseudo-verse — `\p` / `\s1` / `\d` etc. before the
        // first `\v`. Store as verse 0 so the nightly export's `front` key
        // reconstruction in api/src/export.ts emits these markers above
        // `\v 1`. Skipping `front` here was the cause of the
        // live-snapshot diff that dropped chapter-leading `\p` and `\s1`.
        vNum = 0;
      } else {
        const m = verse.match(/^(\d+)(?:-(\d+))?$/);
        if (!m) continue; // skip book-level 'intro' etc.
        vNum = parseInt(m[1], 10);
        // Multi-verse blocks like `\v 6-9` round-trip through verse_end so
        // export emits `\v 6-9` instead of dropping verses 7-9. Inverted
        // ranges (e.g. "9-8") collapse to singleton.
        if (m[2]) {
          const end = parseInt(m[2], 10);
          if (end > vNum) vEnd = end;
        }
      }
      const verseObj = chapterObj[verse];
      const normalized = {
        ...verseObj,
        // Strip outer punctuation, then de-glue any punctuation-spanning `\w`
        // (e.g. `\w out”—the\w*`) — same order as extractVersesForRange.
        verseObjects: splitGluedAlignmentWords(
          normalizeWordPunctuation(verseObj.verseObjects ?? []),
        ),
      };
      const text = extractPlainText(normalized);
      const json_blob = JSON.stringify(normalized);
      lines.push(
        `INSERT INTO verses (book, chapter, verse, verse_end, bible_version, content_json, plain_text) VALUES (${q(book)}, ${q(chNum)}, ${q(vNum)}, ${q(vEnd)}, ${q(bibleVersion)}, ${q(json_blob)}, ${q(text)});`,
      );
      count++;
    }
  }
  return count;
}

// Mirror of `normalizeWordPunctuation` in api/src/importParsers.ts —
// see that file for the rationale. Strips leading / trailing
// non-letter chars off `\w` text, emitting them as adjacent text
// nodes so the aligner doesn't show `"What` or `seeing?"` chips.
const LETTER_RE = /[\p{L}\p{M}\p{N}]/u;
function splitWordPunctuation(text) {
  const first = text.search(LETTER_RE);
  if (first < 0) return { leading: text, core: "", trailing: "" };
  let last = first;
  for (let i = text.length - 1; i >= first; i--) {
    if (LETTER_RE.test(text[i])) { last = i; break; }
  }
  return { leading: text.slice(0, first), core: text.slice(first, last + 1), trailing: text.slice(last + 1) };
}
function normalizeNode(node) {
  if (!node || typeof node !== "object") return [node];
  if (node.type === "word" && node.tag === "w" && typeof node.text === "string") {
    const s = splitWordPunctuation(node.text);
    if (s.leading === "" && s.trailing === "") return [node];
    const out = [];
    if (s.leading) out.push({ type: "text", text: s.leading });
    if (s.core) out.push({ ...node, text: s.core });
    if (s.trailing) out.push({ type: "text", text: s.trailing });
    return out;
  }
  if (Array.isArray(node.children)) {
    return [{ ...node, children: node.children.flatMap(normalizeNode) }];
  }
  return [node];
}
function normalizeWordPunctuation(verseObjects) {
  if (!Array.isArray(verseObjects)) return verseObjects;
  return verseObjects.flatMap(normalizeNode);
}

function extractPlainText(verseObj) {
  // verseObj.verseObjects is the array of tokens. Concatenate text tokens
  // and word tokens, skipping markers. usfm-js verse-objects shape:
  //   { type: 'text', text: '...' }
  //   { type: 'word', text: '...', occurrence, occurrences, ... }
  //   { type: 'milestone', tag: 'zaln-s', children: [...] }
  //   { type: 'paragraph', tag: 'p' }
  const parts = [];
  const walk = (vos) => {
    for (const vo of vos || []) {
      if (vo.text) parts.push(vo.text);
      if (vo.children) walk(vo.children);
    }
  };
  walk(verseObj.verseObjects);
  return parts.join("").replace(/\s+/g, " ").trim();
}

let totalVerses = 0;
