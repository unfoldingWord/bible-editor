// ---------- TSV resources ----------
function parseTsv(srcPath) {
  if (!existsSync(srcPath)) return { headers: [], rows: [] };
  const raw = readFileSync(srcPath, "utf8");
  const linesIn = raw.split(/\r?\n/).filter((l) => l.length > 0);
  if (linesIn.length === 0) return { headers: [], rows: [] };
  const headers = linesIn[0].split("\t");
  const rows = linesIn.slice(1).map((line) => {
    const cells = line.split("\t");
    const o = {};
    headers.forEach((h, i) => (o[h] = cells[i]));
    return o;
  });
  return { headers, rows };
}

function refParts(refRaw) {
  // 'front:intro' -> 0,0
  // '1:intro'     -> 1,0
  // '1:1'         -> 1,1
  // '1:1-3'       -> 1,1 (range collapses to first verse for indexing)
  if (!refRaw) return [0, 0];
  const [ch, vs] = refRaw.split(":");
  const chNum = ch === "front" ? 0 : parseInt(ch, 10) || 0;
  const vsNum = !vs || vs === "intro" ? 0 : parseInt(vs.split("-")[0], 10) || 0;
  return [chNum, vsNum];
}

// Each TSV row also gets a matching edit_log v1 entry. Without this, the
// history endpoint has no record of the imported baseline — so anyone who
// later edits an imported row would lose the ability to view (or revert
// to) the original content. The payload is the same shape the API stores
// for a `create` action via POST /api/rows.
function emitEditLogCreate(kind, rowKey, payload) {
  lines.push(
    `INSERT INTO edit_log (kind, row_key, prev_version, new_version, action, payload_json) VALUES (${q(kind)}, ${q(rowKey)}, NULL, 1, 'create', ${q(JSON.stringify(payload))});`,
  );
}

// tn: Reference, ID, Tags, SupportReference, Quote, Occurrence, Note
let tnCount = 0;
{
  const { rows } = parseTsv(sources.tn);
  for (const r of rows) {
    if (!r.ID) continue;
    const [ch, v] = refParts(r.Reference);
    const occ = r.Occurrence === "" || r.Occurrence == null ? null : parseInt(r.Occurrence, 10) || 0;
    lines.push(
      `INSERT INTO tn_rows (id, book, chapter, verse, ref_raw, tags, support_reference, quote, occurrence, note, sort_order) VALUES (${q(r.ID)}, ${q(book)}, ${q(ch)}, ${q(v)}, ${q(r.Reference)}, ${q(r.Tags || null)}, ${q(r.SupportReference || null)}, ${q(r.Quote || null)}, ${q(occ)}, ${q(r.Note || null)}, ${(tnCount + 1) * 100});`,
    );
    emitEditLogCreate("tn", r.ID, {
      book,
      chapter: ch,
      verse: v,
      ref_raw: r.Reference,
      tags: r.Tags || null,
      support_reference: r.SupportReference || null,
      quote: r.Quote || null,
      occurrence: occ,
      note: r.Note || null,
    });
    tnCount++;
  }
}

