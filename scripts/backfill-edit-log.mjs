// Backfill edit_log v1 entries for tn/tq/twl rows that were imported
// before the importer started writing them. Reads the same TSV files
// import-book.mjs reads, then emits a SQL file with conditional inserts —
// each statement only fires if no v1 entry already exists for that row.
//
// Run:
//   node scripts/backfill-edit-log.mjs ZEC
//   (cd api && npx wrangler d1 execute bible_editor --local --file=../scripts/out/backfill-edit-log-ZEC.sql)
//
// Idempotent: re-running produces the same final state. Rows that already
// have a v1 create/imported entry are skipped at apply time. Rows whose
// content has been edited since import still get the *original* TSV
// values as v1, which is exactly what the history viewer needs to show
// the imported baseline correctly.

import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..");

const book = (process.argv[2] || "").toUpperCase();
if (!book) {
  console.error("usage: node scripts/backfill-edit-log.mjs <BOOK_CODE>   e.g. ZEC");
  process.exit(1);
}

