const samples = resolve(repoRoot, "docs/samples");
const sources = {
  tn: resolve(samples, `en_tn_tn_${book}.tsv`),
  tq: resolve(samples, `en_tq_tq_${book}.tsv`),
  twl: resolve(samples, `en_twl_twl_${book}.tsv`),
};

const q = (v) => {
  if (v === null || v === undefined) return "NULL";
  if (typeof v === "number") return String(v);
  return `'${String(v).replace(/'/g, "''")}'`;
};

function parseTsv(srcPath) {
  if (!existsSync(srcPath)) return { headers: [], rows: [] };
  const raw = readFileSync(srcPath, "utf8");
  const linesIn = raw.split(/\r?\n/).filter((l) => l.length > 0);
  if (linesIn.length === 0) return { headers: [], rows: [] };
  const headers = linesIn[0].split("\t");
  const rows = linesIn.slice(1).map((line) => {
    const cells = line.split("\t");
    const o = {};
    headers.forEach((h, i) => (o[h] = cells[i]));
    return o;
  });
  return { headers, rows };
}

function refParts(refRaw) {
  if (!refRaw) return [0, 0];
  const [ch, vs] = refRaw.split(":");
  const chNum = ch === "front" ? 0 : parseInt(ch, 10) || 0;
  const vsNum = !vs || vs === "intro" ? 0 : parseInt(vs.split("-")[0], 10) || 0;
  return [chNum, vsNum];
}

const out = [];
out.push("-- Auto-generated by scripts/backfill-edit-log.mjs. Do not edit by hand.");
out.push(`-- Book: ${book}`);
out.push(`-- Generated: ${new Date().toISOString()}`);
out.push("BEGIN TRANSACTION;");

// Conditional insert: only adds a v1 entry if none exists for this row in
// any v1-bearing action. Matches both real `create` (POST) and synthetic
// `imported` so re-running this script is a no-op.
function emit(kind, rowKey, payload) {
  out.push(
    `INSERT INTO edit_log (kind, row_key, prev_version, new_version, action, payload_json) ` +
      `SELECT ${q(kind)}, ${q(rowKey)}, NULL, 1, 'create', ${q(JSON.stringify(payload))} ` +
      `WHERE NOT EXISTS (SELECT 1 FROM edit_log WHERE kind = ${q(kind)} AND row_key = ${q(rowKey)} AND new_version = 1);`,
  );
}

let tn = 0;
let tq = 0;
let twl = 0;

{
  const { rows } = parseTsv(sources.tn);
  for (const r of rows) {
    if (!r.ID) continue;
    const [ch, v] = refParts(r.Reference);
    const occ = r.Occurrence === "" || r.Occurrence == null ? null : parseInt(r.Occurrence, 10) || 0;
    emit("tn", r.ID, {
      book,
      chapter: ch,
      verse: v,
      ref_raw: r.Reference,
      tags: r.Tags || null,
      support_reference: r.SupportReference || null,
      quote: r.Quote || null,
      occurrence: occ,
      note: r.Note || null,
    });
    tn++;
  }
}
{
  const { rows } = parseTsv(sources.tq);
  for (const r of rows) {
    if (!r.ID) continue;
    const [ch, v] = refParts(r.Reference);
    const occ = r.Occurrence === "" || r.Occurrence == null ? null : parseInt(r.Occurrence, 10) || 0;
    emit("tq", r.ID, {
      book,
      chapter: ch,
      verse: v,
      ref_raw: r.Reference,
      tags: r.Tags || null,
      quote: r.Quote || null,
      occurrence: occ,
      question: r.Question || null,
      response: r.Response || null,
    });
    tq++;
  }
}
{
  const { rows } = parseTsv(sources.twl);
  for (const r of rows) {
    if (!r.ID) continue;
    const [ch, v] = refParts(r.Reference);
    const occ = r.Occurrence === "" || r.Occurrence == null ? null : parseInt(r.Occurrence, 10) || 0;
    emit("twl", r.ID, {
      book,
      chapter: ch,
      verse: v,
      ref_raw: r.Reference,
      tags: r.Tags || null,
      orig_words: r.OrigWords || null,
      occurrence: occ,
      tw_link: r.TWLink || null,
    });
    twl++;
  }
}

out.push("COMMIT;");

const outDir = resolve(repoRoot, "scripts/out");
mkdirSync(outDir, { recursive: true });
const outPath = resolve(outDir, `backfill-edit-log-${book}.sql`);
writeFileSync(outPath, out.join("\n") + "\n");

console.log(`wrote ${outPath}`);
